        function handleIntersection(entries, observer) {
            entries.forEach((entry, index) => {
                const targetId = entry.target.getAttribute('id');
                const link = document.querySelector(`.sidebar a[href="#${targetId}"]`);

                if (link) {
                    if (entry.isIntersecting && entry.intersectionRatio >= 0.5) {
                        link.classList.add('current');
                        removeCurrentClassFromOtherLinks(link);

                        // Remove "current" class from the first link when the next section is fully visible
                        if (index < sections.length - 1) {
                            const nextSection = sections[index + 1];
                            const nextRect = nextSection.getBoundingClientRect();

                            if (nextRect.top >= 0 && nextRect.bottom <= window.innerHeight) {
                                firstLink.classList.remove('current');
                            }
                        }
                    } else {
                        link.classList.remove('current');
                    }
                }
            });
        }

        // Function to remove the "current" class from other links
        function removeCurrentClassFromOtherLinks(currentLink) {
            const links = document.querySelectorAll('.sidebar a[href^="#"]');
            links.forEach((link) => {
                if (link !== currentLink) {
                    link.classList.remove('current');
                }
            });
        }

